# math module
# math moduli

# https://docs.python.org/3/library/math.html
import math

# constants
print(math.pi)
print(math.e)

# methods
print(math.exp(5))  # e^5
print(math.factorial(6))  # 6!
print(math.sqrt(16))  # v(16)

print(math.log(16, 2))  # log2(16)
print(math.log2(16))
print(math.log10(1000))

print(math.sin(math.pi / 2))
print(math.cos(math.pi))
print(math.tan(0))

